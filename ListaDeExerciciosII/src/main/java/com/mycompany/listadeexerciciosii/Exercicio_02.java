/*
2. Fazer um algoritmo que mostre os números de 1 até 5 e seus
respectivos quadrados
*/

package com.mycompany.listadeexerciciosii;

public class Exercicio_02 {
    
    public static void main(String[] args) {
        
        System.out.println("Os números de 1 a 5 e seus respectivos quadrados: ");
        
        for(int i = 0; i <= 5; i++){
            System.out.println(i + " = " + Math.pow(i, 2));
        }
        
        
        
        
    }
    
}
